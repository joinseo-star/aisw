import streamlit as st

# ------------------------------
#  앱 제목 & 설명
# ------------------------------
st.title("분리수거 도우미 앱 ♻️")
st.write("버리려는 물건 이름을 입력하면 어떤 쓰레기 종류로 버려야 하는지 알려줄게요!")

# ------------------------------
#  분리수거 정보 사전(딕셔너리)
#   - 키: 물건 이름
#   - 값: 분리수거 종류
# ------------------------------
trash_dict = {
    "바나나껍질": "음식물쓰레기",
    "음료수캔": "캔",
    "계란껍질": "음식물쓰레기",
    "콜라캔": "캔",
    "페트병": "플라스틱",
    "생수병": "플라스틱",
    "물병": "플라스틱",
    "종이박스": "종이",
    "신문지": "종이",
    "우유팩": "종이",
    "유리병": "유리",
    "귤껍질": "음식물쓰레기",
    "맥주병": "유리",
    "비닐봉지": "비닐",
    "치킨뼈": "일반쓰레기",
    "과자봉지": "비닐",
    "음식물쓰레기": "음식물쓰레기",
}

# ------------------------------
#  사용자 입력
# ------------------------------
item = st.text_input("버리려는 물건 이름을 입력하세요 (예: 음료수캔, 바나나껍질)")

# ------------------------------
#  버튼 눌렀을 때 결과 출력
# ------------------------------
if st.button("분리수거 종류 확인하기"):
    if not item:
        st.warning("먼저 물건 이름을 입력해주세요 🙂")
    else:
        # 앞뒤 공백 제거
        item_clean = item.strip()
        category = trash_dict.get(item_clean)

        if category:
            st.success(f"'{item_clean}' 은/는 **{category}** 로 버리면 됩니다! ♻️")
        else:
            st.error(f"'{item_clean}' 에 대한 정보가 아직 없어요 😥\n"
                     "단어를 다르게 써보거나, 직접 분리수거 기준을 확인해 주세요.")

# 예시 안내
st.caption("예시: " + ", ".join(list(trash_dict.keys())[:10]) + " ...")
